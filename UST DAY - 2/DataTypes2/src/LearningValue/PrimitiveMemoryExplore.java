package LearningValue;

public class PrimitiveMemoryExplore {
	public static void main(String[] args) {
        System.out.println("--- byte ---");
        System.out.println("Size in bits: " + Byte.SIZE);
        System.out.println("Size in bytes: " + Byte.BYTES);
        
        System.out.println("\n--- short ---");
        System.out.println("Size in bits: " + Short.SIZE);
        System.out.println("Size in bytes: " + Short.BYTES);

        System.out.println("\n--- int ---");
        System.out.println("Size in bits: " + Integer.SIZE);
        System.out.println("Size in bytes: " + Integer.BYTES);

        System.out.println("\n--- long ---");
        System.out.println("Size in bits: " + Long.SIZE);
        System.out.println("Size in bytes: " + Long.BYTES);

        System.out.println("\n--- float ---");
        System.out.println("Size in bits: " + Float.SIZE);
        System.out.println("Size in bytes: " + Float.BYTES);

        System.out.println("\n--- double ---");
        System.out.println("Size in bits: " + Double.SIZE);
        System.out.println("Size in bytes: " + Double.BYTES);

        System.out.println("\n--- char ---");
        System.out.println("Size in bits: " + Character.SIZE);
        System.out.println("Size in bytes: " + Character.BYTES);
    }
}
